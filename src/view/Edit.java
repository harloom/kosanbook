    private void tblEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEditMouseClicked
        // TODO add your handling code here:

        membercontroller.setDateincrement(0);
        int i = tblEdit.getSelectedRow();
        if (i == -1) {
            return;
        }

        String id = (String) tblEdit.getValueAt(i, 0);
        valueID.setText(id);
        String nama = (String) tblEdit.getValueAt(i, 1);
        valueNama.setText(nama);
        String jk = (String) tblEdit.getValueAt(i, 2);
        if (jk.equals("Pria")) {
            radiobperempuan.setSelected(false);
            radiobLaki.setSelected(true);
        } else {
            radiobLaki.setSelected(false);
            radiobperempuan.setSelected(true);
        }

        String alamat = (String) tblEdit.getValueAt(i, 3);
        valueAlamat.setText(alamat);
        String nohp = (String) tblEdit.getValueAt(i, 5);
        valueNohp.setText(nohp);
        String sewa = (String) tblEdit.getValueAt(i, 4);
        valueSewa.setText(sewa);
        String batas = (String) tblEdit.getValueAt(i, 10);
        valueBatas.setText(batas);
        String tipe = (String) tblEdit.getValueAt(i, 6);
        if (tipe.toLowerCase().equals("vip")) {
            buttonGroupTipe.clearSelection();
            radiobVip.setSelected(true);
        } else if (tipe.toLowerCase().equals("standar")) {
            buttonGroupTipe.clearSelection();
            radiobStandard.setSelected(true);
        } else {
            buttonGroupTipe.clearSelection();
            radiobEmpty.setSelected(true);
        }
        String wifi = (String) tblEdit.getValueAt(i, 7);
        if (wifi.toLowerCase().equals("50mbps")) {
            buttonGroupWifi.clearSelection();
            radiob50.setSelected(true);
        } else if (wifi.toLowerCase().equals("30mbps")) {
            buttonGroupWifi.clearSelection();
            radiob30.setSelected(true);
        } else {
            buttonGroupWifi.clearSelection();
            radiob20.setSelected(true);
        }

        //cek harga
        try {
            membercontroller.editCekTotal(this);
        } catch (SQLException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HargaException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblEditMouseClicked
