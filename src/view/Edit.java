    public void datatable() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Nama ");
        tbl.addColumn("Jenis Kelamin");
        tbl.addColumn("Alamat");
        tbl.addColumn("Sewa");
        tbl.addColumn("No HP");
        tbl.addColumn("Tipe Kamar");
        tbl.addColumn("WiFi");
        tbl.addColumn("Total");
        tbl.addColumn("Tanggal Masuk");
        tbl.addColumn("Batas Sewa");
        tblEdit.setModel(tbl);
        try {
            Statement statement = (Statement) konekbyNanda.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from tbl_member");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("id_member"),
                    res.getString("nama"),
                    res.getString("jenis_kelamin"),
                    res.getString("alamat"),
                    res.getString("sewa"),
                    res.getString("nohp"),
                    res.getString("tipe_kamar"),
                    res.getString("tipe_wifi"),
                    res.getString("total"),
                    res.getString("tanggal_masuk"),
                    res.getString("expire"),});
                tblEdit.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

