    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        //ambil api harga //
        try {
            // TODO add your handling code here:

            membercontroller.apiHarga();
        } catch (SQLException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HargaException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }

        //        dekalarasi variable
        String jk,
                tipe,
                wifi;
        Integer totalkamar = null,
                totalinternet = null,
                totalkesuruhan = null;

        String id = valueID.getText();
        String nama = valueNama.getText();
        if (radiobLaki.isSelected()) {
            jk = "Pria";
        } else {
            jk = "Wanita";
        }
        String alamat = valueAlamat.getText();
        String nohp = valueNohp.getText();
        String sewa = valueSewa.getText();
        String batas = valueBatas.getText();

        //cek validation
        if (valueNama.getText().isEmpty() || valueAlamat.getText().isEmpty()
                || valueBatas.getText().isEmpty() || valueSewa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Terdapat form yang kosong"
                    + "\nMohon periksa kembali");
        } else if (valueNohp.getText() != "12" && !(valueNohp.getText().contains("8"))) {
            JOptionPane.showMessageDialog(this, "Karakter No HP harus 12 atau format salah");
        } else {

            ///Selesai Validation 
            if (radiobVip.isSelected()) {
                tipe = "VIP";
                totalkamar = membercontroller.getHrgvip() * Integer.parseInt(sewa);
            } else if (radiobStandard.isSelected()) {
                tipe = "Standar";
                totalkamar = membercontroller.getHrgstd() * Integer.parseInt(sewa);
            } else {
                tipe = "Kosong";
                totalkamar = membercontroller.getHrgempty() * Integer.parseInt(sewa);
            }

            if (radiob50.isSelected()) {
                wifi = "50Mbps";
                totalinternet = membercontroller.getHrgv50() * Integer.parseInt(sewa);
            } else if (radiob30.isSelected()) {
                wifi = "30Mbps";
                totalinternet = membercontroller.getHrgv30() * Integer.parseInt(sewa);
            } else {
                wifi = "20Mbps";
                totalinternet = membercontroller.getHrgv20() * Integer.parseInt(sewa);
            }

            //hitung 
            totalkesuruhan = totalinternet + totalkamar;

            try {
                Statement statement = (Statement) konekbyNanda.getConnection().createStatement();
                statement.executeUpdate("update tbl_member SET total =" + totalkesuruhan + "," + " nama='" + nama + "'," + "jenis_kelamin='" + jk + "'," + "alamat='" + alamat + "'," + "nohp='" + nohp + "'," + "sewa=" + sewa + "," + "expire='" + batas + "'," + "tipe_kamar='" + tipe + "'," + "tipe_wifi='" + wifi + "'" + "WHERE id_member=" + id + "");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            } catch (Exception t) {
                JOptionPane.showMessageDialog(null, "Data gagal diubah");
            }
        }
        datatable();

    }//GEN-LAST:event_btnUbahActionPerformed

